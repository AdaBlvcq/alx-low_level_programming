#include <stdio.h>
/**
* add - add two integers
* sub - subtract two integers
* mul - multiply two integers
* div - divide two integers
* mod - module of two integers
* @i: first integer to add
* @k: second integer to add
* Return: 0 Success
*/
int add(int i, int k)
{
	return (i + k);
}

int sub(int i, int k)
{
	return (i - k);
}

int mul(int i, int k)
{
	return (i * k);
}

int div(int i, int k)
{
	return (i / k);
}

int mod(int i, int k)
{
	return (i % k);
}
